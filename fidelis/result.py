"""Result structures: RowError, DriftReport, ParseResult."""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Optional

from .spec import Spec


@dataclass
class RowError:
    """A single record that did not fit the target model.

    Silently dropping rows is unacceptable — every discarded record is described
    by its row number, the field (if applicable), and the reason.
    """

    row_index: int
    field: Optional[str]
    raw_value: Any
    reason: str
    #: The original source record, kept so the row can be fixed and re-ingested.
    source_row: Optional[dict] = None

    def __str__(self) -> str:
        loc = f"row {self.row_index}"
        if self.field is not None:
            loc += f", field {self.field!r}"
        return f"[{loc}] {self.reason} (raw={self.raw_value!r})"


@dataclass
class DuplicateRow:
    """A row dropped by dedup because it shared a key with a kept row."""

    key: dict
    kept: Any
    dropped: Any

    def __str__(self) -> str:
        return f"duplicate key {self.key} — dropped in favor of the kept row"


@dataclass
class Coverage:
    """How much of the input actually turned into records.

    A single run-level quality score, complementing the per-mapping confidence:
    what fraction of source rows produced at least one valid record.
    """

    rows_in: int = 0            # distinct source rows seen
    rows_with_output: int = 0   # source rows that yielded >= 1 valid record
    rows_with_error: int = 0    # source rows that yielded >= 1 error
    records_out: int = 0        # total valid records (may exceed rows_in via expansion)

    @property
    def score(self) -> float:
        """Fraction of source rows that produced a record (1.0 if no input)."""

        return self.rows_with_output / self.rows_in if self.rows_in else 1.0

    def __str__(self) -> str:
        return (
            f"coverage={self.score:.2f} "
            f"({self.rows_with_output}/{self.rows_in} rows → {self.records_out} records)"
        )


@dataclass
class DriftReport:
    """Mismatch between the actual set of source fields and the spec."""

    has_drift: bool = False
    new_fields: list[str] = field(default_factory=list)
    missing_fields: list[str] = field(default_factory=list)
    action: str = "none"  # ignore | error | regenerate | none

    def __bool__(self) -> bool:
        return self.has_drift

    def describe(self) -> str:
        if not self.has_drift:
            return "No schema drift detected."
        parts = []
        if self.new_fields:
            parts.append(f"new fields: {', '.join(self.new_fields)}")
        if self.missing_fields:
            parts.append(f"missing fields: {', '.join(self.missing_fields)}")
        return f"Schema drift ({self.action}): " + "; ".join(parts)


@dataclass
class ParseResult:
    """Result of a single ``parse()``.

    Attributes:
        valid_rows: Validated records (instances of the target model).
        errors: Records that failed transformation/validation.
        spec_used: The spec that was applied.
        drift_report: Schema mismatches, if any.
        spec_generated: Whether the spec was generated by the LLM in this run.
    """

    valid_rows: list[Any] = field(default_factory=list)
    errors: list[RowError] = field(default_factory=list)
    spec_used: Optional[Spec] = None
    drift_report: DriftReport = field(default_factory=DriftReport)
    spec_generated: bool = False
    #: Rows dropped by dedup (empty unless a dedup key is configured).
    duplicates: list[DuplicateRow] = field(default_factory=list)
    #: Run-level coverage (fraction of source rows that produced a record).
    coverage: Coverage = field(default_factory=Coverage)

    @property
    def needs_review(self) -> bool:
        """Whether the applied spec has any mappings with status ``needs_review``."""

        return bool(self.spec_used and self.spec_used.has_needs_review)

    @property
    def ok(self) -> bool:
        """All rows passed and no review is required."""

        return not self.errors and not self.needs_review

    def summary(self) -> str:
        return (
            f"valid={len(self.valid_rows)} errors={len(self.errors)} "
            f"coverage={self.coverage.score:.2f} "
            f"needs_review={self.needs_review} "
            f"drift={self.drift_report.has_drift} generated={self.spec_generated}"
        )

    # ------------------------------------------------------------------ #
    # Typed / tabular output
    # ------------------------------------------------------------------ #

    def to_dicts(self, *, mode: str = "python") -> list[dict]:
        """Valid rows as plain dicts (``mode="json"`` for JSON-native scalars)."""

        return [r.model_dump(mode=mode) for r in self.valid_rows]

    def errors_to_dicts(self) -> list[dict]:
        """Rejected rows as dicts — for a dead-letter file or a report."""

        return [
            {
                "row_index": e.row_index,
                "field": e.field,
                "reason": e.reason,
                "raw_value": e.raw_value,
            }
            for e in self.errors
        ]

    def quarantine_rows(self) -> list[dict]:
        """Rejected rows as original source records + diagnostic columns."""

        from .quarantine import quarantine_rows

        return quarantine_rows(self)

    def write_quarantine(self, path) -> str:
        """Write rejected rows to ``path`` for a human to fix and re-ingest."""

        from .quarantine import write_quarantine

        return write_quarantine(self, path)

    def to_pandas(self):
        """Valid rows as a ``pandas.DataFrame`` (requires ``fidelis[pandas]``)."""

        try:
            import pandas as pd
        except ImportError as exc:  # pragma: no cover - depends on the environment
            raise ImportError(
                "to_pandas requires pandas. Install: pip install 'fidelis[pandas]'"
            ) from exc
        return pd.DataFrame(self.to_dicts())

    def to_polars(self):
        """Valid rows as a ``polars.DataFrame`` (requires ``fidelis[polars]``)."""

        try:
            import polars as pl
        except ImportError as exc:  # pragma: no cover - depends on the environment
            raise ImportError(
                "to_polars requires polars. Install: pip install 'fidelis[polars]'"
            ) from exc
        return pl.DataFrame(self.to_dicts())
